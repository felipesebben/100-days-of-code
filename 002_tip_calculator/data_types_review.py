# Data Types

# String

print("Hello"[0])

# Subscripting - [n] determines which character of a string you want to print.

print("Hello"[-1])

"123"  # Not a number, just a piece of text.

print("123" + "345")  # Concatenates the two strings.

# Integer - numbers with no decimal place.

123 + 345

# Large Integers - replace commas with underscores (_).
print(123_345 + 344_555)

# Float - floating point number with decimal places.
3.1456

# Boolean - only two possible values (True or False). No quotation marks.
True
False
